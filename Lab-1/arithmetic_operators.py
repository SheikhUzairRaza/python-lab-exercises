# Arithmetic and Boolean Operators in Python

# Arithmetic Operators
# Addition
print(1 + 1)  # Output: 2

# Multiplication
print(2 * 3)  # Output: 6

# Boolean Operators
# Equality operator
print(1 == 0)  # Output: False

# NOT operator
print(not (1 == 0))  # Output: True

# AND operator
print((2 == 2) and (2 == 3))  # Output: False

# OR operator
print((2 == 2) or (2 == 3))  # Output: True