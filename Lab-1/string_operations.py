# String Operations in Python

# Concatenation: Joining two strings using the '+' operator
print('artificial' + "intelligence")  # Output: 'artificialintelligence'

# Changing case
print('artificial'.upper())  # Converts to uppercase: Output -> 'ARTIFICIAL'
print('HELP'.lower())  # Converts to lowercase: Output -> 'help'

# Finding the length of a string
print(len('Help'))  # Output: 4

# Variable Assignment with Strings
s = 'hello world'  # Assigning a string to a variable
print(s)  # Output: 'hello world'

# Using string methods on the variable
print(s.upper())  # Converts the string to uppercase: Output -> 'HELLO WORLD'

# Finding the length of the modified string
print(len(s.upper()))  # Output: 11 (length of 'HELLO WORLD')
